#include "loom/target/arch/amdgpu/feedback_abi.h"

#include "iree/testing/gtest.h"

namespace {

TEST(AmdgpuFeedbackAbiTest, ConstantsMatchHalFeedbackAbi) {
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CONFIG_BYTE_LENGTH, 64u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CONFIG_CHANNEL_BASE_OFFSET, 16u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CONFIG_NOTIFY_SIGNAL_OFFSET, 24u);

  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CHANNEL_BYTE_LENGTH, 64u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CHANNEL_RING_BASE_OFFSET, 16u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CHANNEL_RING_CAPACITY_OFFSET, 24u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CHANNEL_READ_TAIL_OFFSET, 32u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CHANNEL_RESERVATION_HEAD_OFFSET, 40u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_CHANNEL_DROPPED_PACKET_COUNT_OFFSET, 48u);

  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_BYTE_LENGTH, 64u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_HEADER_LENGTH_OFFSET, 4u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_KIND_OFFSET, 6u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_FLAGS_OFFSET, 8u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_STATE_OFFSET, 12u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_SEQUENCE_OFFSET, 16u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_SOURCE_DISPATCH_PTR_OFFSET, 24u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_SOURCE_WORKGROUP_ID_X_OFFSET, 32u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_SOURCE_WORKITEM_ID_X_OFFSET, 36u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_RESERVED0_OFFSET, 40u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_RESERVED1_OFFSET, 44u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_RESERVED_ARRAY_0_OFFSET, 48u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_RESERVED_ARRAY_1_OFFSET, 56u);
}

TEST(AmdgpuFeedbackAbiTest, PacketLengthRoundsUpToAbiAlignment) {
  EXPECT_EQ(loom_amdgpu_feedback_packet_length(0), 64u);
  EXPECT_EQ(loom_amdgpu_feedback_packet_length(1), 128u);
  EXPECT_EQ(loom_amdgpu_feedback_packet_length(63), 128u);
  EXPECT_EQ(loom_amdgpu_feedback_packet_length(64), 128u);
  EXPECT_EQ(loom_amdgpu_feedback_packet_length(65), 192u);
  EXPECT_EQ(loom_amdgpu_feedback_packet_length(
                LOOM_AMDGPU_FEEDBACK_PACKET_MAX_PAYLOAD_LENGTH),
            64u + 16u * 1024u);
}

TEST(AmdgpuFeedbackAbiTest, PacketKindsReserveCommonRuntimeSchemas) {
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_KIND_ASAN, 1u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_KIND_PRINTF, 2u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_KIND_HOST_CALL, 3u);
  EXPECT_EQ(LOOM_AMDGPU_FEEDBACK_PACKET_KIND_USER, 0x8000u);
}

}  // namespace
