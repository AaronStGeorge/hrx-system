#include "loom/target/arch/amdgpu/abi/signal.h"

#include "iree/testing/gtest.h"

namespace {

TEST(AmdgpuSignalAbiTest, ConstantsMatchHalSignalAbi) {
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_BYTE_LENGTH, 64u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_KIND_OFFSET, 0u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_VALUE_OFFSET, 8u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_HARDWARE_DOORBELL_PTR_OFFSET, 8u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_EVENT_MAILBOX_PTR_OFFSET, 16u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_EVENT_ID_OFFSET, 24u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_RESERVED1_OFFSET, 28u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_START_TS_OFFSET, 32u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_END_TS_OFFSET, 40u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_QUEUE_PTR_OFFSET, 48u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_RESERVED3_ARRAY_0_OFFSET, 56u);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_RESERVED3_ARRAY_1_OFFSET, 60u);

  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_KIND_INVALID, 0);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_KIND_USER, 1);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_KIND_DOORBELL, -1);
}

TEST(AmdgpuSignalAbiTest, MailboxConstantsMatchDeviceLibs) {
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_MAILBOX_MESSAGE_ID_GFX10_MASK, 0x7FFFFFu);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_MAILBOX_MESSAGE_ID_GFX9_11_12_MASK, 0xFFFFFFu);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_MAILBOX_MESSAGE_ID_LEGACY_MASK, 0xFFu);
  EXPECT_EQ(LOOM_AMDGPU_SIGNAL_INTERRUPT_SENDMSG, 1u);
}

}  // namespace
