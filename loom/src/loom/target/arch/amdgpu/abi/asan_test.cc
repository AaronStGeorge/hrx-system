#include "loom/target/arch/amdgpu/abi/asan.h"

#include "iree/testing/gtest.h"

namespace {

TEST(AmdgpuAsanAbiTest, ConstantsMatchHalAsanAbi) {
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_BYTE_LENGTH, 96u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_RECORD_LENGTH_OFFSET, 0u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_ABI_VERSION_OFFSET, 4u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_FLAGS_OFFSET, 8u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_SHADOW_SCALE_SHIFT_OFFSET, 12u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_SHADOW_BASE_OFFSET, 16u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_APPLICATION_WINDOW_BASE_OFFSET, 24u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_APPLICATION_WINDOW_SIZE_OFFSET, 32u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_SHADOW_SIZE_OFFSET, 40u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_SHADOW_SLAB_SIZE_OFFSET, 48u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_RESERVED_ARRAY_0_OFFSET, 56u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_RESERVED_ARRAY_1_OFFSET, 64u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_RESERVED_ARRAY_2_OFFSET, 72u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_RESERVED_ARRAY_3_OFFSET, 80u);
  EXPECT_EQ(LOOM_AMDGPU_ASAN_CONFIG_RESERVED_ARRAY_4_OFFSET, 88u);
}

TEST(AmdgpuAsanAbiTest, UsesStandardShadowScaleForAccessChecks) {
  EXPECT_EQ(LOOM_AMDGPU_ASAN_SHADOW_SCALE_SHIFT, 3u);
}

}  // namespace
