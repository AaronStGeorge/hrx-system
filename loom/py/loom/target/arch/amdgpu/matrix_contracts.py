"""AMDGPU matrix contract catalog."""

from __future__ import annotations

from dataclasses import dataclass


@dataclass(frozen=True, slots=True)
class AmdgpuMatrixPayload:
    """Logical matrix payload shape."""

    numeric_type: str
    register_count: int
    element_count: int


@dataclass(frozen=True, slots=True)
class AmdgpuMatrixContract:
    """Target-native matrix contract descriptor row."""

    name: str
    family: str
    features: tuple[str, ...]
    flags: tuple[str, ...]
    tile_shape: tuple[int, int, int]
    lhs: AmdgpuMatrixPayload
    rhs: AmdgpuMatrixPayload
    accumulator: AmdgpuMatrixPayload
    result: AmdgpuMatrixPayload
    scale_kind: str
    wave_size: str = "any"
    low_descriptor_key: str | None = None
    intrinsic_name: str | None = None
    semantic_tag: str | None = None
    fragment_layout: str | None = None


def payload(
    numeric_type: str, register_count: int, element_count: int
) -> AmdgpuMatrixPayload:
    return AmdgpuMatrixPayload(numeric_type, register_count, element_count)


